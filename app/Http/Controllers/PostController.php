    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'nama' => 'required',
            'email' => 'required',
            'username' => 'required',
            'password' => 'required'
        ]);

        //Create New Account
        $sign = new Sign;
        $sign->nama = $request->nama;
        $sign->email = $request->email;
        $sign->username = $request->username;
        $sign->password = $request->password;
        $sign->tanggal = $request->tanggal;
        $sign->agama = $request->agama;
        $sign->gender = $request->gender;
        $sign->alamat = $request->alamat;


        //save meesage
        $sign->save();

        //resirect
        return redirect('/')->with('success', 'Pesan Terkirim');
    }

